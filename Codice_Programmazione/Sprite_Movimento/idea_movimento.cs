public class ch_movement
{
    
    public void ch_movement_UP(/*Posizione*/)
    {
        positionY -= 10;
    }

    public void ch_movement_DOWN(/*Posizione*/)
    {
        positionY += 10;
    }

    public void ch_movement_LEFT(/*Posizione*/)
    {
        positionX -= 10;
    }

    public void ch_movement_RIGHT(/*Posizione*/)
    {
        positionX += 10;
    }
}